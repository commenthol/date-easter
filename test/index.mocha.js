function toNumber (str) {
  return parseInt(str, 10)
}

function split (str) {
  var a = str.split('-')
  return {
    year : toNumber(a[0]),
    month: toNumber(a[1]),
    day  : toNumber(a[2]),
    date : new Date(str + ':00:00')
  }
}

